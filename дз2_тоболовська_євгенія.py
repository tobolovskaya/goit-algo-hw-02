from collections import deque

def is_palindrome(s):
    # Перетворюємо рядок в нижній регістр та видаляємо пробіли
    formatted_s = ''.join(c.lower() for c in s if c.isalnum())

    # Створюємо двосторонню чергу з символів рядка
    char_deque = deque(formatted_s)

    while len(char_deque) > 1:
        if char_deque.popleft() != char_deque.pop():
            return False

    return True

# Тестування функції
test_strings = ["Racecar", "No lemon, no melon", "Python", "А роза упала на лапу Азора"]
for s in test_strings:
    print(f"'{s}' - це паліндром?" , is_palindrome(s))