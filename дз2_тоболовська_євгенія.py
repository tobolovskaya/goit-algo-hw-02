import queue
import random
import time

def generate_request(q):
    request_id = random.randint(1000, 9999)  # Генеруємо унікальний ID заявки
    print(f"Генеруємо заявку з ID: {request_id}")
    q.put(request_id)

def process_request(q):
    if not q.empty():
        request_id = q.get()
        print(f"Обробляємо заявку з ID: {request_id}")
    else:
        print("Черга пуста, немає заявок для обробки.")

# Створюємо чергу заявок
request_queue = queue.Queue()

# Головний цикл програми
try:
    while True:
        user_input = input("Натисніть Enter для генерації заявки, 'p' для обробки заявки або 'q' для виходу: ")
        if user_input.lower() == 'q':
            print("Вихід з програми.")
            break
        elif user_input.lower() == 'p':
            process_request(request_queue)
        else:
            generate_request(request_queue)

        time.sleep(1)
except KeyboardInterrupt:
    print("\nПрограма була перервана користувачем.")

